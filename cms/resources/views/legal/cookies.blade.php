<p>Esta política ayuda a explicar las ocasiones en que CyT Comunicaciones (referido en esta Política como "nosotros", "nos" o "nuestro") puede enviar cookies a los Usuarios del Servicio y por qué. A los fines del presente, se entiende por 'Servicio' a todos los productos de software que integran la oferta de CyT Comunicaciones, junto con nuestro sitio web.</p>
<p>Las cookies son ficheros de información que un sitio web o el prestador de determinados servicios transfiere al dispositivo del Usuario a través del navegador o una aplicación móvil. Las cookies por sí mismas no pueden identificar personalmente al Usuario, pero permiten reconocer su navegador y captar y almacenar determinados datos.</p>
<p>Al usar o acceder al Servicio, el Usuario acepta el uso de cookies de CyT Comunicaciones SRL. de la siguiente manera:</p>

<h2>Información recopilada</h2>
<p>CyT Comunicaciones recibe y registra información de su dispositivo cuando usted utiliza el Servicio. Utilizamos una variedad de métodos, tales como cookies y píxeles de seguimiento, para recopilar esta información, que puede incluir:</p>
<ul>
  <li>Dirección IP</li>
  <li>Identificador único de cookies e información sobre si su dispositivo tiene software para acceder a ciertas funciones</li>
  <li>Identificador único de dispositivo y tipo de dispositivo</li>
  <li>Dominio, tipo de navegador e idioma</li>
  <li>Configuración del sistema operativo y del sistema</li>
  <li>País y zona horaria</li>
  <li>Páginas web visitadas con anterioridad</li>
  <li>Información acerca de su interacción con nuestro Servicio, tales como el comportamiento al hacer clic y las preferencias indicadas</li>
  <li>Tiempos de acceso y URL de referencia</li>
</ul>

<h2>Categorías de cookies</h2>
<p>Generalmente, hay dos categorías de cookies:</p>
<ul>
  <li><strong>Funcionales:</strong> Estas cookies son necesarias para el funcionamiento básico del Servicio y, por lo tanto, siempre están habilitadas. Estas incluyen cookies que le permiten ser recordado mientras explora el Servicio en una sola sesión o, si lo solicita, de sesión en sesión.</li>
  <li><strong>De rendimiento:</strong> Estas cookies nos permiten mejorar la funcionalidad de nuestro Servicio mediante el seguimiento del uso. En algunos casos, estas cookies mejoran la velocidad con la que podemos procesar solicitudes y nos permiten recordar las preferencias del usuario. Rechazar estas cookies puede generar recomendaciones mal adaptadas y un rendimiento lento.</li>
</ul>

<h2>Finalidades del uso de cookies</h2>
<ul>
  <li><strong>Fines analíticos:</strong> Utilizamos cookies para analizar la actividad del Usuario con el fin de mejorar el Servicio. Por ejemplo, al usar cookies podemos observar patrones agregados como el número promedio de búsquedas de contenido.</li>
  <li><strong>Sus preferencias:</strong> Utilizamos cookies para almacenar ciertas preferencias del usuario en el Servicio, como búsquedas recientes realizadas.</li>
</ul>

<h2>Tipos de cookies por duración</h2>
<p>Las <strong>cookies de sesión</strong> son cookies temporales que permanecen en el archivo de cookies de su dispositivo hasta que abandone el Servicio. Las <strong>cookies persistentes</strong> permanecen en el archivo de cookies de su dispositivo durante mucho más tiempo (el tiempo depende de la vida útil de la cookie específica).</p>

<h2>Cookies de terceros</h2>
<p>Son aquellas que se envían al Usuario desde un dominio ajeno a CyT Comunicaciones. Con carácter enunciativo y no limitativo, el usuario acepta en concreto el uso de cookies por parte de Google Analytics y Google Adsense. La información que genera la cookie acerca de su uso del Servicio (incluyendo su dirección IP) será directamente transmitida y archivada por Google en los servidores de Estados Unidos, Brasil y Francia.</p>

<h2>Gestión de cookies</h2>
<p>Usted puede gestionar directamente sus preferencias sobre las cookies. Si desactiva las cookies, es posible que no tenga acceso a algunas funciones que hacen que nuestro Servicio sea más eficiente. Esto se realiza a través de la configuración en cada dispositivo que utilice; consulte las instrucciones de su navegador para habilitar y deshabilitar las cookies.</p>

<h2>Cambios en nuestra política de cookies</h2>
<p>CyT Comunicaciones se reserva el derecho de realizar modificaciones a esta política de cookies. Sugerimos que revise aquí periódicamente para ver si hay cambios; la utilización del Servicio se interpretará como una aceptación a esta Política.</p>

